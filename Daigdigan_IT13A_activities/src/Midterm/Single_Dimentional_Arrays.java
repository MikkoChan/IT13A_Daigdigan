
package Midterm;


public class Single_Dimentional_Arrays {
    
    public static void main(String[] args){
        
        String  Letters[] = {"A","B","C","D","E","F","G"};
        for (int i = 0; i < Letters.length; i++){
        System.out.print(Letters[i]);
        if (i < Letters.length -1) {
            System.out.print(",");
        }
        }
        }
}
        
    
        

